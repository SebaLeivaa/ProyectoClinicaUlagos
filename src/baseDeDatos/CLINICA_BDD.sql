
--Se crea la tabla paciente
CREATE TABLE PACIENTE(
	PACI_NOMBRE VARCHAR(30) NOT NULL,
	PACI_APELLIDO VARCHAR(30) NOT NULL,
	PACI_RUT VARCHAR(10) NOT NULL,
	PACI_FECHANAC DATE NOT NULL,
	PACI_CORREO VARCHAR(60) NOT NULL,
	PACI_CLAVE VARCHAR(30) NOT NULL,
	PACI_GENERO VARCHAR(1),
	PACI_TELEFONO VARCHAR(12) NOT NULL,
	CONSTRAINT PACI_PK PRIMARY KEY (PACI_RUT)
);


--Se crea la tabla especialidad
CREATE TABLE ESPECIALIDAD(
	ESPE_CODIGO NUMERIC(2),
	ESPE_NOMBRE VARCHAR(20) UNIQUE,
	CONSTRAINT ESPE_PK PRIMARY KEY(ESPE_CODIGO)
);

--Se crea la tabla profesional
CREATE TABLE PROFESIONAL(
	PROF_RUT VARCHAR(10) NOT NULL,
	PROF_NOMBRES VARCHAR(20) NOT NULL,
	PROF_APELLIDOS VARCHAR(20) NOT NULL,
	PROF_ESP_CODIGO NUMERIC(2) NOT NULL,
	PROF_CORREO VARCHAR(60) NOT NULL,
	PROF_CLAVE VARCHAR(30) NOT NULL,
	CONSTRAINT PROF_PK PRIMARY KEY (PROF_RUT),
	CONSTRAINT PROF_ESP_FK FOREIGN KEY (PROF_ESP_CODIGO) 
		REFERENCES ESPECIALIDAD
);


--Se crea la tabla Sala
CREATE TABLE SALA(
	SALA_NUMERO NUMERIC(3) NOT NULL,
	CONSTRAINT SALA_PK PRIMARY KEY (SALA_NUMERO)
);

--Se crea la tabla cita_medica
CREATE TABLE CITA_MEDICA(
	CITA_ID SERIAL PRIMARY KEY,
	CITA_DETALLE VARCHAR (800),
	CITA_PACI_RUT VARCHAR(10),
	CITA_PROF_RUT VARCHAR(10) NOT NULL,
	CITA_SALA_NUM NUMERIC(3) NOT NULL,
	CITA_DIS_FECHA TIMESTAMP NOT NULL,
	CITA_DISPONIBLE BOOLEAN NOT NULL,
	CONSTRAINT CITA_PA_FK FOREIGN KEY (CITA_PACI_RUT) 
		REFERENCES PACIENTE(PACI_RUT),
	CONSTRAINT CITA_PRO_FK FOREIGN KEY (CITA_PROF_RUT) 
		REFERENCES PROFESIONAL(PROF_RUT),
	CONSTRAINT CITA_SALA_FK FOREIGN KEY (CITA_SALA_NUM) 
		REFERENCES SALA(SALA_NUMERO),
	CONSTRAINT CITA_UNIQUE UNIQUE (CITA_DIS_FECHA,CITA_SALA_NUM,CITA_PROF_RUT),
	CONSTRAINT CITA_SALA_FECHA_UNIQUE UNIQUE (CITA_DIS_FECHA, CITA_SALA_NUM),
	CONSTRAINT CITA_PROF_FECHA_UNIQUE UNIQUE (CITA_PROF_RUT, CITA_DIS_FECHA)
);

